						<div class="col-sm-9 col-8">
						  <div id="preamble">    
							<h1>PREAMBLE</h1>
							<p>We descendants of Osogbo, currently residing in the United States of America and desirous of promoting
							the community interests and development of Osogbo and its progeny in 
							all aspects, hereby adopt this Constitution to govern the activities of our association.</p>
						  </div>
						  
						  <div id="article1"> 
							<h2>ARTICLE 1: NAME</h2>
							<p>The name of the association shall be the OSOGBO PROGRESSIVE UNION USA, which shall be abbreviated as OPUUSA.
							</p>
						  </div> 
						  
						  <div id="article2">         
							<h2>ARTICLE 2: PURPOSE</h2>
								<hr class="new">
								
								<h4>Section 1:</h4>
								<p>The purpose of the Association shall be to promote the community interests and development of 
								Osogbo and its progeny in all aspects. </p>
								
								<h4>Section 2:</h4>
								<p>
									OPUUSA shall operate as a tax-exempt or not-for-profit organization. To achieve its purpose, 
									OPUUSA may collect membership fees, solicit donations from the general public, apply for grants from 
									governmental or non-governmental 
									organization, and engage in fund raising activities that are consistent with its tax-exempt or not-for-profit status. 
								</p>
								
								<h4>Section 3:</h4>
								<p>
									OPUUSA shall not engage in partisan politics, propaganda, campaign, or lobbying. 
									OPUUSA may however engage in policy initiatives 
									on a nonpartisan and nonsectarian basis, if such initiatives help further its purpose. 
								</p>
								
								<h4>Section 4:</h4>
								<p>
									OPUUSA’s deliberations shall be conducted in Yoruba or English, except in public settings when 
									English shall be the language of expression.
								</p>
						  </div>
						  
						  <div id="article3">         
							<h2>ARTICLE 3: POWERS</h2>
								<hr class="new">
								
								<h4>Section 1:</h4>
								<p>
									OPUUSA shall have all necessary and proper powers to lawfully carry out its purpose, consistently with 
									its tax-exempt or not-for-profit status, including without limitation, the power to employ staff or remunerate any member,
									provided, however, that any remuneration or payment to any OPUUSA member shall conform with a conflict of interest policy that the 
									National Executive Council shall adopt to protect
									OPUUSA’s interests when it enters into a transaction or arrangement that might result in undue excess benefit for the member. 								
								</p>
								
								<h4>Section 2:</h4>
								<p>
									The highest authority of OPUUSA shall be vested in the general membership, which may overrule by a two-third majority vote any 
									decisions taken by the National Executive Council, the Executive Committee or any OPUUSA committees.
								</p>
								
						  </div>
						  
						  <div id="article4">         
							<h2>ARTICLE 4: MEMBERSHIP</h2>
								<hr class="new">
								
								<h4>Section 1:</h4>
								<p>
									Membership of OPUUSA shall be open to all adult descendants of Osogbo, without regard to race, 
									gender, and political or religious affiliation, who subscribe to OPUUSA’s purpose, agree to abide
									by this Constitution or any amendments thereof, and pay OPUUSA membership fees. .
								</p>
								
								<h4>Section 2:</h4>
								<p>
									All individual OPUUSA members shall pay annual membership fees, which shall be one hundred and twenty dollars ($120) at the time 
									of adoption of this Constitution. The general membership may revise this amount by a two-third majority vote. 
								</p>
								
								<h4>Section 3:</h4>
								<p>
									The dues and levies provided under Section 2 above shall be paid by each individual OPUUSA member to his or her local chapter for 
									transmission by the local chapter to OPUUSA's National Account. 
								</p>
								
								<h4>Section 4:</h4>
								<p>
									Any OPUUSA member in a State where there is no local chapter may elect to join a contingent local chapter or remit his or her dues 
									and levies to the National OPUUSA account directly. 
								</p>
								
								<h4>Section 5:</h4>
								<p>
									A member must be in good standing in order to have the power to vote at OPUUSA general meetings or to vote or be voted for during
									OPUUSA elections. 
								</p>
								
								<h4>Section 6:</h4>
								<p>
									For the purposes of this Constitution, a OPUUSA member is in good standing if the member has paid membership fees and levies for the
									current or immediately preceding year and has attended at least one general meeting during the same period. 
								</p>
								
								<h4>Section 7:</h4>
								<p>
									The General Membership may decide by a two-third-majority vote to confer Honorary Membership of OPUUSA on any individual who has 
									contributed substantially to the advancement of OPUUSA or Osogbo. The recipent of such Honorary Membership shall not be entitled to 
									vote or be voted for in OPUUSA elections or meetings. No more than two (2) Honorary Memberships shall be conferred in a given year.
								</p>
						  </div> 
						  
						  <div id="article5">         
							<h2>ARTICLE 5: LOCAL CHAPTERS</h2>
								<hr class="new">
								
								<h4>Section 1:</h4>
								<p>
									If more than five (5) Osogbo descendant reside within a generally recognized metropolitan area where no local chapter 
									of OPUUSA already exists, such individuals may apply in writing to the National Executive Council for recognition as a 
									local chapter of OPUUSA and shall be recognized as such by a simple majority vote of the National Executive Council. 
								</p>
								
								<h4>Section 2:</h4>
								<p>
									Local chapters may adopt constitutions or rules to regulate their own activities so long as no such constitutions or 
									rules are inconsistent with this Constitution. 
								</p>
								
								<h4>Section 3:</h4>
								<p>
									The President and Secretary of each local chapter shall represent the local chapter on the National Executive Council. 
								</p>
								
								<h4>Section 4:</h4>
								<p>
									Local chapters shall be responsible to facilitate the collection of OPUUSA membership fees or levies from members of their
									respective chapters.
								</p>
								
						  </div> 
						  
						  <div id="article6">         
							<h2>ARTICLE 6: DISCIPLINE</h2>
								<hr class="new">
								
								<h4>Section 1:</h4>
								<p>
									Members of OPUUSA shall conduct themselves with decorum at meetings and behave as good ambassadors of Osogbo and 
									OPUUSA at all times. 
								</p>
								
								<h4>Section 2:</h4>
								<p>
									Any member may be sanctioned for gross misconduct that reflect adversely on OPUUSA or Osogbo. The National Executive
									Council shall set up a committee to inquire into the alleged misconduct. The committee shall have 30 days from its inception date to give the accused member an opportunity to be heard and shall submit its findings and recommendations to the National Executive Council, which shall then determine, with a simple majority vote, the appropriate sanction, except that a member may not be suspended or removed from OPUUSA unless and until the National Executive Council, upon finding specifically that the misconduct warrants suspension or removal, submits the matter to the General Membership 
									and the General Membership approves the suspension or removal by a two-third-majority vote.
								</p>
						  </div>

						  <div id="article7">         
							<h2>ARTICLE 7: MEETINGS</h2>
								<hr class="new">
								
								<h4>Section 1:</h4>
								<p>
									All meetings may be conducted in person, telephonically or via video-conferencing or other electronic
									media, and participants may
									vote at such meetings without regard to their physical location at the time of the meeting. 
								</p>
								
								<h4>Section 2:</h4>
								<p>
									A General Membership meeting of OPUUSA shall be held at least three (3) times in each calendar year.
									The General Secretary at the direction of the President shall consult with the Executive Committee and the National 
									Executive Council to prepare and circulate an agenda for a General Membership meeting with reasonable notice. 
								</p>
								
								<h4>Section 3:</h4>
								<p>
									The President, the General Secretary at the direction of the President, or a simple majority of the Executive Committee 
									may convene an emergency General Membership meeting at any time. 
								</p>
								
								<h4>Section 4:</h4>
								<p>
									A member in good standing may request an emergency General Membership meeting by sending a written request that sets forth 
									the basis for the request to the President or the Secretary General, who shall then have fifteen days to call 
									the meeting or refer the request to the Executive Committee for approval or disapproval by a simple majority vote. 
								</p>
								
								<h4>Section 5:</h4>
								<p>
									Unless any provision of this Constitution specifically states otherwise, issues arising at any meeting of OPUUSA, 
									the National Executive Council, the Executive Committee or any OPUUSA committees, shall be resolved by a simple majority vote. 
									Each member shall have one and only one vote at any such meeting, 
									except that, in the event of a tied vote, the vote of the chairperson shall decide the issue subject to the vote.  
								</p>
								
								<h4>Section 6:</h4>
								<p>
									Only members of the Executive Committee shall have the power to vote during meetings of the Executive Committee.
								</p>
								
								<h4>Section 7:</h4>
								<p>
									Only members of the National Executive Council shall have the power to vote during meetings of the National Executive Council. 
								</p>
								
								<h4>Section 8:</h4>
								<p>
									Only members of OPUUSA committee shall have the power to vote during meetings of those committees. 
								</p>
								
								<h4>Section 9:</h4>
								<p>
									A motion to vote on an issue must be seconded by another member before a vote is cast.  
								</p>
								
								<h4>Section 10:</h4>
								<p>
									Fifty percent of the members with power to vote shall form a quorum at meetings of the National Executive Council, the Executive Committee and all other 
									committees and sub-committees of the organization. 
								</p>
								
								<h4>Section 11:</h4>
								<p>
									At least ten (10) members in good standing, including at least two (2) members of the Executive Committee, shall for a quorum at OPUUSA general meetings. 
								</p>
								
								<h4>Section 12:</h4>
								<p>
								If no quorum is formed at any meeting, the meeting shall stand adjourned and shall be reconvened with reasonable notice and the members present at such 
								reconvened meeting shall be deemed to form a quorum.
								</p>
						  </div> 
						  
						  <div id="article8">         
							<h2>ARTICLE 8: MEETINGS</h2>
								<hr class="new">
								
								<h4>Section 1:</h4>
								<p>
									A national convention to which all OPUUSA members shall be invited shall be held, if possible,
									every two (2) years, in the month of July or August.
								</p>
								
								<h4>Section 2:</h4>
								<p>	The Executive Committee shall prepare and present a comprehensive report of the activities of OPUUSA to 
									the General Membership at each convention. 
								</p>
								
								<h4>Section 3:</h4>
								<p>
									Venue for the Convention shall be on a rotational basis using the alphabetical order of the local chapters' names. 
								</p>
								
								<h4>Section 4:</h4>
								<p>
									In the event the local chapter determined to host the convention on the rotational basis is unready and/or unwilling to host the convention, 
									other local chapters shall have the right to host the convention in the same alphabetical order continuing from 
									the local chapter unready and/or unwilling to host the convention.
								</p>
						  </div> 
						  
						  <div id="article9">         
							<h2>ARTICLE 9: ELECTIONS</h2>
								<hr class="new">
								
								<h4>Section 1:</h4>
								<p>
									Elections of all Executive Committee members shall take place every two years at the National Convention if one is held. If however, 
									a National Convention cannot be held as set forth in Article 8 above, elections of
									Executive Committee members shall be held at a General Membership meeting held in lieu of the Convention. 
								</p>
								
								<h4>Section 2:</h4>
								<p>
									No less than 6 months prior to the elections date, The President with approval of the National Executive Council shall 
									appoint an election committee to organize open and fair elections, during which members in good standing may vote in person, telephonically or via 
									video-conferencing or other electronic media such as text messages or emails, without regard to their physical locations. 
								</p>
								
								<h4>Section 3:</h4>
								<p>
									No less than 3 days prior to the elections date, the election committee, in consultation with the National 
									Executive Council and the leadership of each local chapter, shall prepare and ultimately certify and publish 
									to the General Membership a candidates' list and a voters' register consisting of members in good standing who are eligible to vote. Upon the certification, 
									no changes shall be made to the candidates list or the voters' register until the elections are completed. 
								</p>
								
								<h4>Section 4:</h4>
								<p>
									All members of the Executive Committee shall be elected to a two-year term of office by a simple majority vote of members in good standing. 
								</p>
								
								<h4>Section 5:</h4>
								<p>
									A term of office shall start after the official proclamation of the election results in an election year and end after the official proclamation of the election result in the next election year. 
									No person shall serve in the same Executive Committee position for more than two consecutive terms. 
								</p>
								
								<h4>Section 6:</h4>
								<p>
									If a vacancy occurs on the Executive Committee at any time and for any reason, the Executive Committee may vote by simple majority to 
									appoint any member of the organization who is in good standing to fill the position for the duration of the term with all privileges
									and rights of an elected officer. Regardless of its duration, such substitution term shall count as a full term for 
									purposes of determining eligibility to run for office under the two-consecutive-terms limit provision of section 5 of this Article.
								</p>
						  </div>
						  
						  <div id="article10">         
							<h2>ARTICLE 10: EXECUTIVE COMMITTEE</h2>
								<hr class="new">
								
								<h4>Section 1:</h4>
								<p>
									An Executive Committee shall manage the affairs of OPUUSA. The Executive Committee shall consist of a National President (thePresident), 
									a First Vice President, a Second Vice President, a National General Secretatry (the General Secretary), a National Assistant General 
									Secretary (the Assistant General Secretary), a National Treasurer (the Treasurer), a Financial Secretary (The Financial Secretary), 
									a National Public Relations 
									Officer (the Public Relations Officer), a National Auditor (the Auditor) and two (2) ex-officio members.  
								</p>
								
								<h4>Section 2: The President shall:</h4>
								<p>
									(a) Chair all General Membership meetings and all meetings of the Executive Committee or the National Executive Council, 
									except that a two-third majority of the National Executive Council may appoint a disinterested member within its ranks to preside over a 
									General Membership meeting or a meeting of the National Executive Council; (b) Provide leadership to the Executive Committee, the National
									Executive Council and OPUUSA in general; (c) Represent the Executive Committee and speak on behalf of OPUUSA at public functions and activities; 
									(d) Sign documents on behalf of OPUUSA, except that either the General Secretary or the Treasurer must co-sign OPUUSA checks; (e) Keep members 
									informed of OPUUSA's activities, and maintain working relations with donors; and 
									(f) Engage in other functions to achieve OPUUSA's purpose. 
								</p>
								
								<h4>Section 3: The First Vice President shall:</h4>
								<p>
									(a) Assist the President; (b) Carry out the functions of the President in the President's absence or incapacity; and (c) Perform and other
									functions delegated to him or her by the President, the Executive Committee, the 
									National Executive Council or the General Membership. 
								</p>
								
								<h4>Section 4: The Second Vice President shall:</h4>
								<p>
									(a) Assist the President or the First Vice President; (b) Carry out the functions of the President in the absence or incapacity of the
									President and the First Vice President; and (c) Perform any other functions delegated to him or her by the president, the First Vice President, 
									the Executive Committee, the National Executive Council or the General Membership. 
								</p>
								
								<h4>Section 5: The General Secretary shall:</h4>
								<p>
									(a) Maintain an up-to-date membership register with members' contact information; (b) Call General Membership meetings, Executive Committee 
									meetings or National Executive Council meetings at the direction of the President, prepared agendas for such meetings in consultation with
									the President and maintain a record of delibrations and attendance at the meetings; (c) Serve as a signatory of OPUUSA's accounts; 
									(d) Maintain the organization's official records; (e) Perform any other functions delegated 
									to him or her by the President, the Executive Committee, the National Executive Council or the General Membership.
								</p>
								
								<h4>Section 6: The Assistant General Secretary shall:</h4>
								<p>
									(a) Assist the General Secretary; (b) Assume the duties of the General Secretary in the absence or disability or the General Secretary; 
									(c) Perform any other functions delegated to him or her by the President, 
									the Executive Committee, the National Executive Council or the General Membership. 
								</p>
								
								<h4>Section 7: The Treasurer shall:</h4>
								<p>
									(a) Be the Custodian of OPUUSA's funds, which shall be kept in one or more bank accounts with the President, the General Secretary and the 
									Treasurer as account signatories; (b) Pay all bills approved by the Executive Committee, the National Executive Council or General Membership; 
									(c) Ensure that all tax returns are prepared and filed as required under the law; and (d) Perform any other functions delegated to him or her by the President, 
									the Executive Committee, the National Executive Council or the General Membership. 
								</p>
								
								<h4>Section 8: The Public Relations Officer shall:</h4>
